    public SelectMap(PacmanWindow pacmanWindow, Font font){

        super();
        this.pacmanWindow = pacmanWindow;
        this.font = font;

        setLayout(new BoxLayout(this,BoxLayout.Y_AXIS));
        setBackground(Color.BLACK);

        add(Box.createVerticalGlue());
        headerLabel = headerLabel();
        add(headerLabel);
        add(Box.createVerticalStrut(30));
        add(map1Button());
        add(Box.createVerticalStrut(10));
        add(map2Button());
        add(Box.createVerticalStrut(10));
        add(map3Button());
        add(Box.createVerticalStrut(10));
        add(map4Button());
        add(Box.createVerticalStrut(10));
        add(map5Button());
        add(Box.createVerticalStrut(10));
        add(backButton());
        add(Box.createVerticalGlue());
    }

    public void displayGameOver(){
        var displayingGameOverThread = new Thread(() -> {
            try{
                headerLabel.setText("GAME OVER");
                headerLabel.setForeground(Color.RED);
                revalidate();
                Thread.sleep(3000);
                headerLabel.setText("Choose your map");
                headerLabel.setForeground(Color.YELLOW);
            } catch (InterruptedException ex){
                System.out.println("displaying game over thread was interrupted - " + ex.getMessage());
            }

        });

        displayingGameOverThread.start();
    }

    public JLabel headerLabel(){
        JLabel header = new JLabel("Choose your map");
        header.setFont(font.deriveFont(Font.BOLD,28f));
        header.setAlignmentX(CENTER_ALIGNMENT);
        header.setForeground(Color.YELLOW);

        return header;
    }

